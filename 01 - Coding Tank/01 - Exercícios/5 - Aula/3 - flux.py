opcao = int(input('escolha uma bebida: 1 - Coca, 2 - Pepsi, 3 - Guaraná, 4 - Chá'))
if opcao == 1:
    print('Você escolheu Coca-Cola')
elif opcao == 2:
    print('Você escolheu Pepsi')
elif opcao == 3:
    print('Você escolheu Guaraná')
elif opcao == 4:
    print('Você escolheu Chá')
else:
    print('Opção inválida')